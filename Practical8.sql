CREATE TABLE OrderTable (
    Order_no NUMBER PRIMARY KEY, -- Primary key for the table
    Amount NUMBER, -- Amount of the order
    Odate DATE, -- Order date
    Cnum NUMBER, -- Customer number (foreign key)
    Snum NUMBER, -- Salesperson number (foreign key)
    CONSTRAINT fk_Customer_Order FOREIGN KEY (Cnum) REFERENCES Customer(Cnum), -- Unique constraint name
    CONSTRAINT fk_Salesperson_Order FOREIGN KEY (Snum) REFERENCES Salespeople(Snum) -- Unique constraint name
);